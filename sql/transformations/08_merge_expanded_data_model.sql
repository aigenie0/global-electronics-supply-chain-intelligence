-- FIRST_MONTH: 2023-01-01
-- LAST_MONTH: 2024-12-01
-- MONTH_COUNT: 24
-- PRODUCT_COUNT: 2


-- Confirm monthly coverage for each year and product

SELECT
    D.YEAR,
    F.HS_CODE,
    P.PRODUCT_GROUP,
    COUNT(*) AS FACT_ROW_COUNT,
    COUNT(DISTINCT F.TRADE_MONTH) AS MONTH_COUNT,
    COUNT(DISTINCT F.PARTNER_CODE) AS PARTNER_COUNT

FROM ANALYTICS.FACT_SEMICONDUCTOR_TRADE AS F

LEFT JOIN ANALYTICS.DIM_DATE AS D
    ON F.TRADE_MONTH = D.TRADE_MONTH

LEFT JOIN ANALYTICS.DIM_PRODUCT AS P
    ON F.HS_CODE = P.HS_CODE

GROUP BY
    D.YEAR,
    F.HS_CODE,
    P.PRODUCT_GROUP

ORDER BY
    D.YEAR,
    F.HS_CODE;


