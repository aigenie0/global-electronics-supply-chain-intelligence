-- Check for duplicate fact-table grain combinations

WITH DUPLICATE_GROUPS AS (
    SELECT
        TRADE_MONTH,
        REPORTER_CODE,
        PARTNER_CODE,
        FLOW_CODE,
        HS_CODE,
        COUNT(*) AS ROW_COUNT

    FROM ANALYTICS.FACT_SEMICONDUCTOR_TRADE

    GROUP BY
        TRADE_MONTH,
        REPORTER_CODE,
        PARTNER_CODE,
        FLOW_CODE,
        HS_CODE

    HAVING COUNT(*) > 1
)

SELECT
    COUNT(*) AS DUPLICATE_GROUP_COUNT,

    IFF(
        COUNT(*) = 0,
        'PASS: No duplicate fact-table grain detected',
        'FAIL: Duplicate fact-table grain detected'
    ) AS VALIDATION_RESULT

FROM DUPLICATE_GROUPS;


-- Expected:
-- DUPLICATE_GROUP_COUNT: 0
-- VALIDATION_RESULT: PASS


-- Show annual reported World totals by product

SELECT
    D.YEAR,
    F.HS_CODE,
    P.PRODUCT_GROUP,

    SUM(F.TRADE_VALUE_USD)
        AS REPORTED_WORLD_IMPORT_VALUE_USD,

    COUNT(DISTINCT F.TRADE_MONTH)
        AS MONTH_COUNT

FROM ANALYTICS.FACT_SEMICONDUCTOR_TRADE AS F

LEFT JOIN ANALYTICS.DIM_DATE AS D
    ON F.TRADE_MONTH = D.TRADE_MONTH

LEFT JOIN ANALYTICS.DIM_PRODUCT AS P
    ON F.HS_CODE = P.HS_CODE

WHERE F.IS_AGGREGATE = TRUE
  AND F.PARTNER_CODE = 'World'

GROUP BY
    D.YEAR,
    F.HS_CODE,
    P.PRODUCT_GROUP

ORDER BY
    D.YEAR,
    F.HS_CODE;


-- Expected:
-- Four rows, each with MONTH_COUNT = 12


