-- Populate the date dimension with one row per trade month

MERGE INTO ANALYTICS.DIM_DATE AS TARGET

USING (
    WITH DISTINCT_MONTHS AS (
        SELECT DISTINCT
            TRADE_MONTH

        FROM STAGING.US_MEMORY_IMPORTS_2024_CLEAN
    )

    SELECT
        TRADE_MONTH,
        YEAR(TRADE_MONTH) AS YEAR,
        MONTH(TRADE_MONTH) AS MONTH_NUMBER,
        TO_CHAR(TRADE_MONTH, 'MMMM') AS MONTH_NAME,
        'Q' || QUARTER(TRADE_MONTH) AS QUARTER,
        TO_CHAR(TRADE_MONTH, 'YYYY-MM') AS YEAR_MONTH,

        COUNT(*) OVER (
            PARTITION BY YEAR(TRADE_MONTH)
        ) = 12 AS IS_COMPLETE_YEAR

    FROM DISTINCT_MONTHS
) AS SOURCE

ON TARGET.TRADE_MONTH = SOURCE.TRADE_MONTH

WHEN MATCHED THEN
    UPDATE SET
        TARGET.YEAR = SOURCE.YEAR,
        TARGET.MONTH_NUMBER = SOURCE.MONTH_NUMBER,
        TARGET.MONTH_NAME = SOURCE.MONTH_NAME,
        TARGET.QUARTER = SOURCE.QUARTER,
        TARGET.YEAR_MONTH = SOURCE.YEAR_MONTH,
        TARGET.IS_COMPLETE_YEAR = SOURCE.IS_COMPLETE_YEAR

WHEN NOT MATCHED THEN
    INSERT (
        TRADE_MONTH,
        YEAR,
        MONTH_NUMBER,
        MONTH_NAME,
        QUARTER,
        YEAR_MONTH,
        IS_COMPLETE_YEAR
    )
    VALUES (
        SOURCE.TRADE_MONTH,
        SOURCE.YEAR,
        SOURCE.MONTH_NUMBER,
        SOURCE.MONTH_NAME,
        SOURCE.QUARTER,
        SOURCE.YEAR_MONTH,
        SOURCE.IS_COMPLETE_YEAR
    );


-- Populate the semiconductor trade fact table

MERGE INTO ANALYTICS.FACT_SEMICONDUCTOR_TRADE AS TARGET

USING (
    SELECT
        TRADE_MONTH,
        COALESCE(REPORTER_ISO, REPORTER_COUNTRY) AS REPORTER_CODE,
        COALESCE(SUPPLIER_ISO, SUPPLIER_COUNTRY) AS PARTNER_CODE,
        'M' AS FLOW_CODE,
        HS_CODE,

        SUM(TRADE_VALUE_USD) AS TRADE_VALUE_USD,
        SUM(QUANTITY) AS QUANTITY,
        SUM(NET_WEIGHT_KG) AS NET_WEIGHT_KG,

        MAX(IFF(IS_AGGREGATE, 1, 0)) = 1 AS IS_AGGREGATE,
        MAX(IFF(IS_REPORTED, 1, 0)) = 1 AS IS_REPORTED,

        'HS as reported by UN Comtrade' AS CLASSIFICATION_CODE,
        'us_memory_ic_imports_monthly_2024_pilot.csv' AS SOURCE_FILE

    FROM STAGING.US_MEMORY_IMPORTS_2024_CLEAN

    GROUP BY
        TRADE_MONTH,
        COALESCE(REPORTER_ISO, REPORTER_COUNTRY),
        COALESCE(SUPPLIER_ISO, SUPPLIER_COUNTRY),
        HS_CODE
) AS SOURCE

ON  TARGET.TRADE_MONTH = SOURCE.TRADE_MONTH
AND TARGET.REPORTER_CODE = SOURCE.REPORTER_CODE
AND TARGET.PARTNER_CODE = SOURCE.PARTNER_CODE
AND TARGET.FLOW_CODE = SOURCE.FLOW_CODE
AND TARGET.HS_CODE = SOURCE.HS_CODE

WHEN MATCHED THEN
    UPDATE SET
        TARGET.TRADE_VALUE_USD = SOURCE.TRADE_VALUE_USD,
        TARGET.QUANTITY = SOURCE.QUANTITY,
        TARGET.NET_WEIGHT_KG = SOURCE.NET_WEIGHT_KG,
        TARGET.IS_AGGREGATE = SOURCE.IS_AGGREGATE,
        TARGET.IS_REPORTED = SOURCE.IS_REPORTED,
        TARGET.CLASSIFICATION_CODE = SOURCE.CLASSIFICATION_CODE,
        TARGET.SOURCE_FILE = SOURCE.SOURCE_FILE

WHEN NOT MATCHED THEN
    INSERT (
        TRADE_MONTH,
        REPORTER_CODE,
        PARTNER_CODE,
        FLOW_CODE,
        HS_CODE,
        TRADE_VALUE_USD,
        QUANTITY,
        NET_WEIGHT_KG,
        IS_AGGREGATE,
        IS_REPORTED,
        CLASSIFICATION_CODE,
        SOURCE_FILE
    )
    VALUES (
        SOURCE.TRADE_MONTH,
        SOURCE.REPORTER_CODE,
        SOURCE.PARTNER_CODE,
        SOURCE.FLOW_CODE,
        SOURCE.HS_CODE,
        SOURCE.TRADE_VALUE_USD,
        SOURCE.QUANTITY,
        SOURCE.NET_WEIGHT_KG,
        SOURCE.IS_AGGREGATE,
        SOURCE.IS_REPORTED,
        SOURCE.CLASSIFICATION_CODE,
        SOURCE.SOURCE_FILE
    );


