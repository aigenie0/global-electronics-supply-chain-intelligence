-- Check for duplicate rows at the defined fact-table grain

WITH DUPLICATES AS (
    SELECT
        TRADE_MONTH,
        REPORTER_CODE,
        PARTNER_CODE,
        FLOW_CODE,
        HS_CODE,
        COUNT(*) AS ROW_COUNT

    FROM ANALYTICS.FACT_SEMICONDUCTOR_TRADE

    GROUP BY
        TRADE_MONTH,
        REPORTER_CODE,
        PARTNER_CODE,
        FLOW_CODE,
        HS_CODE

    HAVING COUNT(*) > 1
)

SELECT
    COUNT(*) AS DUPLICATE_GROUP_COUNT,

    IFF(
        COUNT(*) = 0,
        'PASS: No duplicate fact-table grain detected',
        'FAIL: Duplicate fact-table records detected'
    ) AS VALIDATION_RESULT

FROM DUPLICATES;


-- Compare individual supplier totals with the reported World total

WITH SUPPLIER_TOTAL AS (
    SELECT
        SUM(TRADE_VALUE_USD) AS SUPPLIER_IMPORT_VALUE_USD

    FROM ANALYTICS.FACT_SEMICONDUCTOR_TRADE

    WHERE IS_AGGREGATE = FALSE
),

WORLD_TOTAL AS (
    SELECT
        SUM(TRADE_VALUE_USD) AS REPORTED_WORLD_TOTAL_USD

    FROM ANALYTICS.FACT_SEMICONDUCTOR_TRADE

    WHERE IS_AGGREGATE = TRUE
)

SELECT
    S.SUPPLIER_IMPORT_VALUE_USD,
    W.REPORTED_WORLD_TOTAL_USD,

    S.SUPPLIER_IMPORT_VALUE_USD
        - W.REPORTED_WORLD_TOTAL_USD AS DIFFERENCE_USD

FROM SUPPLIER_TOTAL AS S

CROSS JOIN WORLD_TOTAL AS W;
