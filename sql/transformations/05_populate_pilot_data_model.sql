-- Test the complete star-schema join

SELECT
    F.TRADE_MONTH,
    D.YEAR,
    D.MONTH_NAME,
    D.QUARTER,

    REPORTER.COUNTRY_NAME AS REPORTER_COUNTRY,
    SUPPLIER.COUNTRY_NAME AS SUPPLIER_MARKET,

    P.PRODUCT_GROUP,
    F.FLOW_CODE,
    F.TRADE_VALUE_USD,
    F.IS_AGGREGATE

FROM ANALYTICS.FACT_SEMICONDUCTOR_TRADE AS F

LEFT JOIN ANALYTICS.DIM_DATE AS D
    ON F.TRADE_MONTH = D.TRADE_MONTH

LEFT JOIN ANALYTICS.DIM_COUNTRY AS REPORTER
    ON F.REPORTER_CODE = REPORTER.COUNTRY_CODE

LEFT JOIN ANALYTICS.DIM_COUNTRY AS SUPPLIER
    ON F.PARTNER_CODE = SUPPLIER.COUNTRY_CODE

LEFT JOIN ANALYTICS.DIM_PRODUCT AS P
    ON F.HS_CODE = P.HS_CODE

ORDER BY
    F.TRADE_MONTH,
    F.TRADE_VALUE_USD DESC

LIMIT 30;


